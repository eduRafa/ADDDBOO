    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        if (JOptionPane.showConfirmDialog(rootPane, "Â¿Seguro que quieres borrar esto?",
                "Borrar Area", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {

            try {
                Long id = (Long) tabla.getValueAt(tabla.getSelectedRow(), 0);
                if (id != -1) {
                    bookC.deleteBook(id);
                }
                CrearModelo();
                Cargar_Informacion();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        if (jTextField2.getText().length() > 0) {
            Book b = new Book();
            if (!idLabel.getText().equals("-1")) {
                b = bookC.getBook(Long.parseLong(idLabel.getText()));
            }
            b.setTitle(jTextField2.getText());
            if (modelAutor.getSize() > 0) {
                List<Author> autores = new ArrayList<>();
                for (int i = 0; i < modelAutor.size(); i++) {
                    autores.add((Author) modelAutor.getElementAt(i));
                }
                b.setAuthors(autores);
            } else {
                b.setAuthors(null);
            }
            b.setPages(Integer.parseInt(jTextField3.getText()));
            if (idLabel.getText().equals("-1")) {
                bookC.insertBooks(b);
            } else {
                bookC.editBooks(b);
            }
            limpiar();
            botonGuardar.setEnabled(false);
            tabla.clearSelection();
            botonGuardar.setEnabled(false);
            botonCancelar.setEnabled(false);
            CrearModelo();
            Cargar_Informacion();
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

